public class LoopWhile {
    
    public static void main(String[] args) {
        
                    // while (condition) {
                    // code block to be executed
                    // }  -> if의 확장팩 느낌. 
        
        int first = 5 ;
        int second = 10; 
        while(first < second) {
            System.out.println(first) ;
            first = first + 1 ; 
        }
        
        System.out.println() ;

        // return 0;
    }
}
